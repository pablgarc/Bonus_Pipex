#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	res;
	int				sig;

	res = 0;
	sig = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sig *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = (res * 10) + (*str - '0');
		str++;
	}
	res = res * sig;
	return (res);
}
